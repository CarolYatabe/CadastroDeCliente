     public void atualizarCliente(Cliente novoCliente){
        this.conectar.conectaBanco();
        
        String consultaCpf = this.cpf_atualizar.getText();
        
        try {
            this.conectar.updateSQL(
                "UPDATE cadastroclientes SET "                    
                    + "nome = '" + nome_atualizar.getText() + "',"
                    + "email = '" + email_atualizar.getText() + "',"
                    + "telefone = '" + telefone_atualizar.getText() + "',"
                    + "cidade = '" + cidade_atualizar.getText() + "',"                   
                    + "uf = '" + uf_atualizar_combobox.getSelectedItem() + "'"
                + " WHERE "
                    + "cpf = '" + consultaCpf + "'"
                + ";"
            );
        } catch(Exception e){
            System.out.println("Erro ao atualizar cliente " +  e.getMessage());
            JOptionPane.showMessageDialog(null, "Erro ao atualizar cliente");
        } finally {
            this.conectar.fechaBanco();
            limparCamposAtualizar();
            JOptionPane.showMessageDialog(null, "Cliente atualizado com sucesso");
        }
    }  

